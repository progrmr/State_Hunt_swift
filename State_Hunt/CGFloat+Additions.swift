extension CGFloat {
    init(_ value: UInt32) {
        self.init( UInt(value) )
    }
}

extension CGFloat {
    init(_ value: UInt16) {
        self.init( UInt(value) )
    }
}

extension CGFloat {
    init(_ value: UInt8) {
        self.init( UInt(value) )
    }
}

extension CGFloat {
    init(_ value: Int32) {
        self.init( Int(value) )
    }
}

extension CGFloat {
    init(_ value: Int16) {
        self.init( Int(value) )
    }
}

extension CGFloat {
    init(_ value: Int8) {
        self.init( Int(value) )
    }
}

